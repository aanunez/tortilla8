	ld  va, 0                ; init x cord
	ld  vb, 0                ; init y cord
	ld  vc, 1                ; inc for i reg
    ld  i,  mandelbrot       ; dot for drawing
loop:
    drw va, vb, 1            ; Draw
    add i, vc                ; Inc the Index
    add va, 8                ; Inc x by 1 byte
    se  va, 64               ; If x == 64, skip jp
    jp  loop                 ; Jump back to draw rest of line
    ld  va, 0                ; reset va
    add vb, 1                ; Inc y by 1 to draw the next line
    se  vb, 32               ; If y == 32, skip jp
    jp  loop                 ; Jump back to draw another line
spin:
    jp  spin                 ; Finished

